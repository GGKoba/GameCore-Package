/// <summary>
/// Тип модификатора параметра.
/// </summary>
public enum StatModifierType
{
    Flat,       // Прямое добавление значения, например +2
    Percent,    // Процент от значения, например +15%
    Special     // Особый эффект, не влияет напрямую на расчёт, используется в условиях/описании
}
